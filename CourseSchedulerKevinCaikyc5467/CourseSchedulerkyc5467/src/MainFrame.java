    private void DropStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentSubmitButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry dropText = (StudentEntry)DropStudentComboBox.getSelectedItem();
        DropStudentTextArea.setText(dropText.getLastName() + ", " + dropText.getFirstName() + " " + dropText.getStudentID() + " has been dropped from the list of students.\n\n");
        String studentID = dropText.getStudentID();
        String firstName = dropText.getFirstName();
        String lastName = dropText.getLastName();
        
        ArrayList<String> semesterList = SemesterQueries.getSemesterList();
        
        for (String semester : semesterList) { //Iterate through courses and drop one by one, make sure to replace the drops with adds
            DropStudentTextArea.append("For Semester: " + semester + "\n");
            ArrayList<ScheduleEntry> courses = ScheduleQueries.getScheduleByStudent(semester, studentID);
            
            for(ScheduleEntry course : courses){
                String courseCode = course.getCourseCode();
                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, courseCode);
                DropStudentTextArea.append(firstName + " " + lastName + " has been dropped from " + courseCode + "\n");

                if (course.getStatus().equals("S")) {
                    ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByClass(semester, courseCode);
                    if (!waitlisted.isEmpty()) {
                        ScheduleEntry firstOnList = waitlisted.get(0);
                        ScheduleQueries.updateScheduleEntry(firstOnList);
                        StudentEntry nextOnList = StudentQueries.getStudent(firstOnList.getStudentID());
                        DropStudentTextArea.append(nextOnList.getFirstName() + " " + nextOnList.getLastName() + " has been scheduled for " + courseCode + "\n");
                    }
                }
            }
            DropStudentTextArea.append("\n");
        }
        
        StudentQueries.dropStudent(studentID);        
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_DropStudentSubmitButtonActionPerformed

