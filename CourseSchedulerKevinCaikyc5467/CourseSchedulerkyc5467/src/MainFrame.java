    public void rebuildSemesterComboBoxes() { //Updates the semester combo box when adding a new semester
        ArrayList<String> semesters = SemesterQueries.getSemesterList(); //new ArrayList item semesters from the semester query
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray())); //Updates the semester combo box
        if (!semesters.isEmpty()) { //If else statement for updating the semster label
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes() { //Updates the courses combo box when adding a new course code
        ArrayList<String> courses = CourseQueries.getAllCourseCodes(); //new ArrayList item courses from the course query
        ClassCourseCodeCombo.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
    }
    
    public void rebuildClassComboBoxes() {
        String semester = (String) currentSemesterComboBox.getSelectedItem();
        ArrayList<String> courseCodes = ClassQueries.getAllCourseCodes(semester);
        SelectClassCombo.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        ChooseClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        DropClassAdminCombo.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        DropClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
    }
    
