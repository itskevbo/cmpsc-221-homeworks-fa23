    private void ScheduleSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScheduleSubmitActionPerformed
        // TODO add your handling code here:
        String semester = currentSemester;
        StudentEntry student = (StudentEntry) SelectStudentCombo.getSelectedItem();
        String studentID = student.getStudentID();
        String courseCode = (String) SelectClassCombo.getSelectedItem();
        Timestamp timestamp = new Timestamp(System.currentTimeMillis());

        int enrolled = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);
        int total = ClassQueries.getClassSeats(currentSemester, courseCode);

        if (enrolled < total){
            ScheduleEntry entry = new ScheduleEntry(semester, courseCode, studentID, "S", timestamp);
            ScheduleQueries.addScheduleEntry(entry);
            ScheduleCourseStatusLabel.setText(student.toString() + " has been scheduled for this class.");
        } else {
            ScheduleEntry entry = new ScheduleEntry(semester, courseCode, studentID, "W", timestamp);
            ScheduleQueries.addScheduleEntry(entry);
            ScheduleCourseStatusLabel.setText(student.toString() + " has been waitlisted for this class.");
        }
    }//GEN-LAST:event_ScheduleSubmitActionPerformed

